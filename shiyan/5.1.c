//大数相加
#include<stdio.h>
#include<string.h>
#define N 100  // 定义常量N，作为数组最大长度，支持处理最多100位的超大整数

// 函数声明：实现两个超大整数的加法计算
// A[]：存储第一个超大整数（逆序存储）
// B[]：存储第二个超大整数（逆序存储）
// result[]：存储加法计算的结果（逆序存储）
int calculate(int A[],int B[],int result[]);

// 主函数：程序入口，负责数据读取、调用计算函数、结果输出
int main(){
    int A[N]={0},B[N]={0};  // 定义两个数组存储两个超大整数（初始化为0）
    char strA[N]={0},strB[N]={0};  // 定义字符数组存储输入的数字字符串
    int lenA=0,lenB=0;  // 记录两个数字字符串的长度
    
    scanf("%s",strA);  // 输入第一个超大整数（以字符串形式）
    scanf("%s",strB);  // 输入第二个超大整数（以字符串形式）
    
    lenA=strlen(strA);  // 获取第一个数字字符串的长度
    lenB=strlen(strB);  // 获取第二个数字字符串的长度
    
    // 将第一个数字字符串逆序转换为整数数组
    // 例如：字符串"123" → 数组A[0]=3, A[1]=2, A[2]=1（低位在前，方便进位计算）
    for(int i=0;i<lenA;i++){
        A[i]=strA[lenA-1-i]-'0';  // 字符转数字：减去'0'的ASCII值
    }
    
    // 将第二个数字字符串逆序转换为整数数组
    // 例如：字符串"456" → 数组B[0]=6, B[1]=5, B[2]=4（低位在前，方便进位计算）
    for(int i=0;i<lenB;i++){
        B[i]=strB[lenB-1-i]-'0';  // 字符转数字：减去'0'的ASCII值
    }
    
    int result[N]={0};  // 定义结果数组，存储加法结果（初始化为0）
    calculate(A,B,result);  // 调用计算函数，执行超大整数加法
    
    int start=0;  // 记录结果数组中有效数字的起始索引（跳过前置0）
    // 从数组末尾向前遍历，找到第一个非0数字，确定结果的起始位置
    for(int i=N-1;i>=0;i--){
        if(result[i]!=0){
            start=i;
            break;
        }
    }
    
    // 从有效起始位置向前遍历，逆序输出结果（恢复正常数字顺序）
    for(int j=start;j>=0;j--){
        printf("%d",result[j]);
    }
    
    return 0;  // 程序正常结束
}

// 函数实现：超大整数加法的核心逻辑（低位在前，逐位相加并处理进位）
int calculate(int A[],int B[],int result[]){
    // 遍历数组的每一位（最多N位，覆盖所有可能的数字长度）
    for(int i=0;i<N;i++){
        // 判断当前位相加是否产生进位（和大于等于10）
        if(A[i]+B[i]>=10){
            result[i]=A[i]+B[i]-10;  // 存储当前位的个位数字
            A[i+1]+=1;  // 向高位（i+1位）进1（直接累加到A数组的下一位，简化计算）
        }
        else{
            result[i]=A[i]+B[i];  // 无进位，直接存储两位之和
        }
    }
    return 0;  // 计算完成，返回0
}